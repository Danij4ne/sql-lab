
-- Basic unit of measure = tables

-- In SQL, for each column we must indicate the data type of the values it will store.

-- Basic data types:
-- int      = number
-- varchar  = text
-- decimal  = decimal number
-- datetime = date with time
-- DATE     = date without time

-- Common column attributes / constraints:
-- PK = PRIMARY KEY -> the unique id (all of this can also be done using code)
-- NN = NOT NULL    -> force a column to not be empty
-- UQ = UNIQUE      -> value cannot be repeated
-- B  = BINARY      -> binary value
-- UN = UNSIGNED    -> unsigned (no negative values)
-- ZF = ZEROFILL    -> pads the number with zeros
-- AI = AUTO_INCREMENT -> auto-increment
-- G  = GENERATED COLUMN

-- VARCHAR(50) -> can only store up to 50 characters in a string
